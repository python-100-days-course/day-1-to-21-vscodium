# Data types
string = "Hello"
print(string[0]) #subscripting, prints 1st character in the string
print(123_456_789) #_ instead of , can be used in integers, ignored
print(3.14159) #float, data type
print(True) #Boolean, data type
print(type(string)) #display data type

integer = 1234
print('Variable "integer" is ' + str(type(integer)) + " data type.")
conv_integer = str(integer) #convert to string
print('Variable "conv_integer" is ' + str(type(conv_integer)) + " data type.")
